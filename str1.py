if __name__ == '__main__':
    str = input()
if (str.isalnum()):
    print("true")
else:
    print("false")
if (str.isalpha()):
    print("true")
else:
    print("false")
if (str.isdigit()):
    print("true")
else:
    print("false")
if (str.islower()):
    print("true")
else:
    print("false")
if (str.isupper()):
    print("true")
else:
    print("false")
